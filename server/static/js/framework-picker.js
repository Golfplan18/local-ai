/* V3 Input Handling Phase 4 — framework picker dropdown.
 *
 * Listens for `ora:input-toolbar:framework` (emitted by the input-pane
 * toolbar's framework button), opens a picker overlay anchored to the top
 * of the output pane, and renders rows fetched from
 * GET /api/frameworks/picker. Search-as-you-type filters in place. On row
 * click the picker commits the framework via OraInputState.setFramework
 * and closes itself. Outside-click and Escape close without committing.
 *
 * Three category groups are supported per design doc Q3:
 *   - "standard"     — built-in Ora frameworks (not removable)
 *   - "user-created" — user-authored masters (X to remove from picker;
 *                      optional delete-entirely)
 *   - "one-off"      — generated by master frameworks (X-to-remove with
 *                      delete-entirely)
 *
 * For now the picker API only emits "standard"; the other groups will
 * appear automatically when those provenance sources land. Empty groups
 * are hidden.
 *
 * Mode picker is intentionally absent. Mode selection is being redesigned
 * end-to-end and will land its own UI separately.
 */
(() => {
  const PICKER_SELECTOR = '#frameworkPicker';
  const SEARCH_SELECTOR = '#frameworkPickerSearch';
  const LIST_SELECTOR   = '#frameworkPickerList';
  const TOOLBAR_BTN_SELECTOR = '#inputToolbarFramework';

  const CATEGORY_LABELS = {
    'standard':      'Standard Ora',
    'user-created':  'User-created',
    'one-off':       'One-offs',
  };
  const CATEGORY_ORDER = ['standard', 'user-created', 'one-off'];

  let _picker = null;
  let _list   = null;
  let _search = null;
  let _btn    = null;
  let _frameworks = [];
  let _outsideHandler = null;
  let _escapeHandler  = null;

  // ── Open / close ────────────────────────────────────────────────────

  function open() {
    if (!_picker) return;
    _picker.hidden = false;
    if (_btn) _btn.setAttribute('aria-expanded', 'true');

    // Fetch rows fresh on every open — frameworks can change as the user
    // installs / authors / generates them. Cheap call: it's a directory
    // scan of frameworks/book/.
    fetch('/api/frameworks/picker')
      .then(res => res.ok ? res.json() : { frameworks: [] })
      .then(payload => {
        _frameworks = Array.isArray(payload && payload.frameworks)
          ? payload.frameworks : [];
        render(_frameworks, '');
      })
      .catch(err => {
        console.warn('[framework-picker] fetch failed:', err);
        _list.innerHTML = '';
        const empty = document.createElement('div');
        empty.className = 'framework-picker__empty';
        empty.textContent = 'Could not load frameworks. Check the server log.';
        _list.appendChild(empty);
      });

    if (_search) {
      _search.value = '';
      // Defer focus so the click that opened us doesn't immediately blur.
      setTimeout(() => { try { _search.focus(); } catch (_) {} }, 0);
    }

    _outsideHandler = (e) => {
      if (_picker.hidden) return;
      const t = e.target;
      if (_picker.contains(t) || (_btn && _btn.contains(t))) return;
      close();
    };
    _escapeHandler = (e) => {
      if (e.key === 'Escape' && !_picker.hidden) {
        e.stopPropagation();
        close();
      }
    };
    document.addEventListener('mousedown', _outsideHandler, true);
    document.addEventListener('keydown', _escapeHandler, true);
  }

  function close() {
    if (!_picker || _picker.hidden) return;
    _picker.hidden = true;
    if (_btn) _btn.setAttribute('aria-expanded', 'false');
    if (_outsideHandler) {
      document.removeEventListener('mousedown', _outsideHandler, true);
      _outsideHandler = null;
    }
    if (_escapeHandler) {
      document.removeEventListener('keydown', _escapeHandler, true);
      _escapeHandler = null;
    }
  }

  function toggle() {
    if (!_picker) return;
    if (_picker.hidden) open();
    else close();
  }

  // ── Render ──────────────────────────────────────────────────────────

  function render(rows, query) {
    if (!_list) return;
    _list.innerHTML = '';
    const q = (query || '').trim().toLowerCase();
    const filtered = q
      ? rows.filter(r =>
          (r.display_name || '').toLowerCase().includes(q) ||
          (r.display_description || '').toLowerCase().includes(q))
      : rows;

    if (filtered.length === 0) {
      const empty = document.createElement('div');
      empty.className = 'framework-picker__empty';
      empty.textContent = q
        ? `No frameworks match "${query}".`
        : 'No frameworks available.';
      _list.appendChild(empty);
      return;
    }

    // Group by category, in declared order. Empty groups are skipped.
    for (const cat of CATEGORY_ORDER) {
      const groupRows = filtered.filter(r => (r.category || 'standard') === cat);
      if (groupRows.length === 0) continue;
      const group = document.createElement('div');
      group.className = 'framework-picker__group';
      group.dataset.category = cat;

      const label = document.createElement('div');
      label.className = 'framework-picker__group-label';
      label.textContent = CATEGORY_LABELS[cat] || cat;
      group.appendChild(label);

      for (const row of groupRows) group.appendChild(buildRow(row));
      _list.appendChild(group);
    }
  }

  function buildRow(framework) {
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'framework-picker__row';
    btn.dataset.frameworkId = framework.id;
    btn.dataset.category    = framework.category || 'standard';

    const body = document.createElement('div');
    body.className = 'framework-picker__row-body';

    const title = document.createElement('div');
    title.className = 'framework-picker__row-title';
    title.textContent = framework.display_name || framework.id;
    body.appendChild(title);

    const desc = document.createElement('div');
    desc.className = 'framework-picker__row-desc';
    desc.textContent = framework.display_description || '';
    body.appendChild(desc);

    btn.appendChild(body);

    // X-to-remove only for non-standard categories. Standard frameworks
    // ship with Ora and cannot be picker-removed.
    if (framework.category && framework.category !== 'standard') {
      const remove = document.createElement('button');
      remove.type = 'button';
      remove.className = 'framework-picker__remove';
      remove.setAttribute('aria-label', 'Remove framework from picker');
      remove.innerHTML = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><line x1="6" y1="6" x2="18" y2="18"/><line x1="18" y1="6" x2="6" y2="18"/></svg>';
      remove.addEventListener('click', (e) => {
        e.stopPropagation();
        showRemoveConfirm(btn, framework);
      });
      btn.appendChild(remove);
    }

    btn.addEventListener('click', () => {
      commitSelection(framework);
    });

    return btn;
  }

  function showRemoveConfirm(rowEl, framework) {
    // Drop a confirmation row in place of the framework row for this
    // entry only. Two actions: "Remove from picker" and "Delete entirely".
    const confirm = document.createElement('div');
    confirm.className = 'framework-picker__confirm';
    confirm.innerHTML = `
      <div class="framework-picker__confirm-text">Remove "${framework.display_name}" from the picker, or delete the framework file entirely?</div>
      <div class="framework-picker__confirm-actions">
        <button type="button" class="framework-picker__confirm-btn" data-confirm="cancel">Cancel</button>
        <button type="button" class="framework-picker__confirm-btn" data-confirm="hide">Remove from picker</button>
        <button type="button" class="framework-picker__confirm-btn framework-picker__confirm-btn--danger" data-confirm="delete">Delete entirely</button>
      </div>
    `;
    rowEl.replaceWith(confirm);
    confirm.querySelectorAll('[data-confirm]').forEach(b => {
      b.addEventListener('click', (e) => {
        const action = e.currentTarget.dataset.confirm;
        // Hide-from-picker and delete-entirely both require a server-side
        // action that doesn't exist yet. Surface a console note for now;
        // when the framework-management endpoint lands, replace these
        // fetches with the real call. Cancel just re-renders the row.
        if (action === 'hide' || action === 'delete') {
          console.info(
            `[framework-picker] ${action} requested for`,
            framework.id,
            '— framework-management endpoint not yet implemented; tracking via design.'
          );
        }
        // Re-render the list either way (cancel restores the row;
        // hide/delete will become real removals once the endpoint lands).
        render(_frameworks, _search ? _search.value : '');
      });
    });
  }

  function commitSelection(framework) {
    if (window.OraInputState && typeof window.OraInputState.setFramework === 'function') {
      window.OraInputState.setFramework(framework);
    }
    document.dispatchEvent(new CustomEvent('ora:framework-selected', {
      detail: { framework },
    }));
    close();
  }

  // ── Toolbar button .is-active state ──────────────────────────────────

  function syncToolbarState(framework) {
    if (!_btn) return;
    if (framework) _btn.classList.add('is-active');
    else _btn.classList.remove('is-active');
  }

  // ── Boot ─────────────────────────────────────────────────────────────

  function init() {
    _picker = document.querySelector(PICKER_SELECTOR);
    _search = document.querySelector(SEARCH_SELECTOR);
    _list   = document.querySelector(LIST_SELECTOR);
    _btn    = document.querySelector(TOOLBAR_BTN_SELECTOR);
    if (!_picker || !_search || !_list) {
      console.warn('[framework-picker] DOM nodes missing; picker disabled.');
      return;
    }

    _search.addEventListener('input', () => {
      render(_frameworks, _search.value);
    });

    document.addEventListener('ora:input-toolbar:framework', () => {
      toggle();
    });

    // Reflect external state changes (e.g., bridge label or future code
    // clearing the framework on submit) back onto the toolbar button.
    document.addEventListener('ora:framework-changed', (e) => {
      syncToolbarState(e.detail && e.detail.framework);
    });

    // Initial state sync — in case the page loads with a framework
    // already set in OraInputState (it never does today, but cheap to
    // be correct).
    if (window.OraInputState && typeof window.OraInputState.getFramework === 'function') {
      syncToolbarState(window.OraInputState.getFramework());
    }
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  // Tiny public surface for tests / external callers (Phase 5 bridge,
  // Phase 7 setup popup).
  window.OraFrameworkPicker = { open, close, toggle };
})();
