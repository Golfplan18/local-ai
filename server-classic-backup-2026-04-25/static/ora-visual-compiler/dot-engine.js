/**
 * ora-visual-compiler / dot-engine.js
 * Shared Graphviz-WASM DOT-rendering primitive.
 *
 * This file is NOT a renderer. It is a utility module that wraps the vendored
 * @viz-js/viz (Graphviz compiled to WebAssembly) and exposes a clean async
 * contract for turning DOT strings into SVG. Any renderer whose target is a
 * directed-graph layout consumes this primitive; it does not call viz-js
 * directly.
 *
 * Consumers (all scheduled to land after WP-1.2c):
 *   - renderers/causal-dag.js       (WP-1.2c, this package) — DAGitty → DOT
 *   - renderers/influence-diagram.js (WP-1.3e)               — semantic JSON → DOT
 *   - renderers/ibis.js              (WP-1.3i)               — IBIS grammar → DOT
 *   - renderers/concept-map.js       (WP-1.3k)               — Novak map → DOT
 *   - any future renderer that needs DOT (fallback paths, migrations)
 *
 * Contract:
 *   - `init()` is idempotent, lazy, returns a Promise<Viz> singleton.
 *   - `dotToSvg(dot, options?)` is async, never throws, always returns
 *     { svg: string, errors: Array, warnings: Array }. On parse failure svg
 *     is '' and errors contains one E_DSL_PARSE entry.
 *   - On success the returned SVG has been stripped of inline style/fill/
 *     stroke/font-family attributes so semantic CSS from ora-visual-theme.css
 *     can take over. An `ora-visual` class is added to the root <svg>.
 *   - The module exposes itself as window.OraVisualCompiler._dotEngine.
 *
 * Load order:
 *   errors.js
 *   validator.js
 *   renderers/stub.js
 *   dispatcher.js
 *   index.js
 *   vendor/viz-js/viz-standalone.js      <- provides window.Viz
 *   dot-engine.js                        <- this file (depends on window.Viz)
 *   renderers/<dot-consumer>.js          <- e.g. causal-dag.js
 *
 * Typical usage (from a renderer):
 *
 *   const { dotToSvg } = window.OraVisualCompiler._dotEngine;
 *   const dot = 'digraph G { rankdir=LR; a -> b; b -> c }';
 *   const { svg, errors, warnings } = await dotToSvg(dot);
 *   if (errors.length) return { svg: '', errors, warnings };
 *   return { svg: wrapWithOraClasses(svg, envelope), errors: [], warnings };
 *
 * Depends on: errors.js, vendor/viz-js/viz-standalone.js (window.Viz)
 */

window.OraVisualCompiler = window.OraVisualCompiler || {};

window.OraVisualCompiler._dotEngine = (function () {

  const { CODES, make } = window.OraVisualCompiler.errors;

  // Singleton promise. All callers await the same instance.
  let _instancePromise = null;

  /**
   * init() → Promise<Viz>
   * Idempotent. First caller triggers WASM compile; subsequent callers
   * receive the cached promise. Viz.instance() resolves to an object with
   * renderString(input, { format, engine }) and render(...) methods.
   */
  function init() {
    if (_instancePromise) return _instancePromise;

    if (typeof window.Viz === 'undefined' ||
        typeof window.Viz.instance !== 'function') {
      _instancePromise = Promise.reject(new Error(
        '[OraVisualCompiler] dot-engine: window.Viz not found. ' +
        'Load vendor/viz-js/viz-standalone.js before this file.'
      ));
      return _instancePromise;
    }

    _instancePromise = window.Viz.instance();
    return _instancePromise;
  }

  /**
   * _stripInlineStyles(svg) → svg
   *
   * Graphviz emits hard-coded style / fill / stroke / font-family / font-size
   * attributes on generated elements. Semantic CSS must be able to override
   * them without !important, so we strip them before returning.
   *
   * We preserve geometry (points, d, x, y, width, height, cx, cy, r, etc.)
   * and anything inside xml/text namespaces. Attributes removed:
   *   fill, stroke, stroke-width, font-family, font-size, font-weight,
   *   font-style, style, color.
   *
   * The root <svg> gets the `ora-visual` class (renderer appends the
   * type-specific class outside this function).
   */
  function _stripInlineStyles(svg) {
    if (typeof svg !== 'string' || svg.length === 0) return svg;

    const STRIP_ATTRS = [
      'fill', 'stroke', 'stroke-width', 'stroke-dasharray',
      'font-family', 'font-size', 'font-weight', 'font-style',
      'style', 'color',
    ];

    let out = svg;

    // Drop any <?xml?> declaration (renderers embed; this causes parse
    // noise in browsers when concatenated).
    out = out.replace(/<\?xml[^?]*\?>\s*/g, '');

    // Drop DOCTYPE svg declarations (same reason).
    out = out.replace(/<!DOCTYPE[^>]*>\s*/gi, '');

    // Drop <!-- Generated by graphviz ... --> preamble comments.
    out = out.replace(/<!--[^]*?-->/g, '');

    // Graphviz HTML-entity-escapes hyphens and some other characters that
    // appear in attribute values. The entity is valid XML but it breaks
    // downstream CSS selectors (".ora-visual__node" will not match the
    // serialized "ora&#45;visual__node"). Decode the numeric entities that
    // Graphviz emits in attribute positions. We limit the set to the
    // characters Graphviz actually produces: hyphen, less-than, greater-
    // than, ampersand (ampersand kept as &amp; if it was escaped from a
    // literal ampersand — safe to decode here because our ids and classes
    // never contain a literal ampersand).
    out = out.replace(/&#45;/g, '-')
             .replace(/&#x2d;/gi, '-');

    // Strip each inline-style attribute. Whitespace-tolerant regex; handles
    // both double- and single-quoted values.
    for (const a of STRIP_ATTRS) {
      // Match:  <WS>attr<WS>=<WS>"..."  or  <WS>attr<WS>=<WS>'...'
      const re = new RegExp(
        '\\s' + a + '\\s*=\\s*"[^"]*"',
        'gi'
      );
      const re2 = new RegExp(
        '\\s' + a + '\\s*=\\s*\'[^\']*\'',
        'gi'
      );
      out = out.replace(re, '').replace(re2, '');
    }

    // Add the `ora-visual` class to the root <svg ...> if not already there.
    // The renderer will extend with `ora-visual--<type>` and other classes
    // via its own wrap step — this just guarantees the base class is present.
    out = out.replace(/<svg\b([^>]*?)>/i, function (m, attrs) {
      if (/\sclass\s*=\s*"[^"]*"/.test(attrs)) {
        return '<svg' + attrs.replace(
          /\sclass\s*=\s*"([^"]*)"/,
          function (_, existing) {
            if (existing.split(/\s+/).indexOf('ora-visual') >= 0) {
              return ' class="' + existing + '"';
            }
            return ' class="ora-visual ' + existing + '"';
          }
        ) + '>';
      }
      return '<svg' + attrs + ' class="ora-visual">';
    });

    return out;
  }

  /**
   * dotToSvg(dot, options?) → Promise<{ svg, errors, warnings }>
   *
   * options:
   *   engine  — Graphviz layout engine: 'dot' (default), 'neato', 'fdp',
   *             'sfdp', 'twopi', 'circo'. Renderers pick what fits the type.
   *
   * On parse failure the returned object has svg === '' and a single
   * E_DSL_PARSE error; never throws. Warnings returned by Graphviz itself
   * (e.g. "trouble in init_rank") are attached as non-fatal warnings with
   * code E_DSL_PARSE-severity-warning… actually we use W_STUB_RENDERER is
   * wrong; we deliberately don't coerce Graphviz warnings — those appear
   * in the SVG as comments if at all. Parse-level warnings surface on
   * the RenderResult; we filter only for status === 'failure'.
   */
  async function dotToSvg(dot, options) {
    options = options || {};
    const engine = options.engine || 'dot';

    if (typeof dot !== 'string' || dot.length === 0) {
      return {
        svg: '',
        errors: [make(CODES.E_DSL_PARSE,
          'dot-engine: empty DOT input')],
        warnings: [],
      };
    }

    let viz;
    try {
      viz = await init();
    } catch (err) {
      return {
        svg: '',
        errors: [make(CODES.E_DSL_PARSE,
          'dot-engine: viz-js failed to initialize — ' +
          (err && err.message ? err.message : String(err)))],
        warnings: [],
      };
    }

    // viz.render returns { status, output, errors } — status === 'failure'
    // means DOT did not parse. We convert to our { svg, errors, warnings }
    // shape without ever throwing.
    let result;
    try {
      result = viz.render(dot, { format: 'svg', engine: engine });
    } catch (err) {
      return {
        svg: '',
        errors: [make(CODES.E_DSL_PARSE,
          'dot-engine: Graphviz threw on render — ' +
          (err && err.message ? err.message : String(err)))],
        warnings: [],
      };
    }

    if (!result || result.status !== 'success' ||
        typeof result.output !== 'string') {
      const gErrs = (result && result.errors) || [];
      const msg = gErrs.length
        ? gErrs.map(function (e) {
            return (e && e.message) ? e.message : String(e);
          }).join(' | ')
        : 'Graphviz returned failure with no error detail';
      return {
        svg: '',
        errors: [make(CODES.E_DSL_PARSE,
          'dot-engine: DOT parse/layout failed — ' + msg)],
        warnings: [],
      };
    }

    // Success. Strip inline styles and add the base class.
    const cleaned = _stripInlineStyles(result.output);
    return { svg: cleaned, errors: [], warnings: [] };
  }

  return {
    init: init,
    dotToSvg: dotToSvg,
    // Exposed for unit-testing only. Not part of the stable API surface.
    _stripInlineStyles: _stripInlineStyles,
  };
}());
